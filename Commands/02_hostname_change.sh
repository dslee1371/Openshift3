#!/bin/sh

. ./00_env.sh

echo -e "[S]================================================================================"
for hostdomain in `cat ${OCP_HOSTNAME}`
do
    echo -e "-----------------------------------------------------------------------------------"
    echo -e "@@@[HOST NAME CHANGE] ==> ${hostdomain}"
    ssh -tt root@${hostdomain}  " \
        hostnamectl set-hostname ${hostdomain}; \
        hostname;"
    echo -e "-----------------------------------------------------------------------------------"
done
echo -e "================================================================================[E]"
